# ╔═╡ d6420530-f61c-4360-8e42-1a171f4bb046
function feed_edge(g::SimpleDiGraph{V}, w::Dict{Edge{V}, W}, i_pc::V, j_pc::V, duration::W) where {V <: TYPE_VERTEX, W <: TYPE_WEIGHT}
    edge = Edge(i_pc, j_pc)
    if haskey(w, edge)
        w[edge] += duration
    else
        w[edge] = duration
    end
    add_edge!(g, edge)
end

